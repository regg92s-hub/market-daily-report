def _over_50ma(ratio_series, resample_rule):
    """Er ratioen over sin egen 50-perioders MA paa gitt tidsramme?
    Returnerer (over: bool|None, dist_pct: float|None)."""
    r = ratio_series.resample(resample_rule).last().dropna()
    if len(r) < 52:
        return None, None
    ma = r.rolling(50).mean()
    if pd.isna(ma.iloc[-1]):
        return None, None
    last = float(r.iloc[-1]); ma_now = float(ma.iloc[-1])
    return (last > ma_now), ((last-ma_now)/ma_now*100)

